function adicao() {
    var num1 = 1;
    var num2 = 2;
    var resultado =  num1 + num2;
    console.log("Resultado da soma: "+ num1 + " + " + num2 + " = " + resultado);
}
function sub() {
    var num1 = 11;
    var num2 = 2;
    var resultado =  num1 - num2;
    console.log("Resultado da subtração : "+ num1 + " - " + num2 + " = " + resultado);
}
function mult() {
    var num1 = 9;
    var num2 = 2;
    var resultado =  num1 * num2;
    console.log("Resultado da multplicação : "+ num1 + " * " + num2 + " = " + resultado);
}
function div() {
    var num1 = 8;
    var num2 = 2;
    var resultado =  num1 / num2;
    console.log("Resultado da divisão : "+ num1 + " / " + num2 + " = " + resultado);
}

